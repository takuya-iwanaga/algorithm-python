import random

maze=[
[9]*9,
[9,0,0,0,9,9,0,9,9],
[9,0,9,9,0,0,0,0,9],
[9,0,0,0,0,9,9,9,9],
[9,9,0,9,0,9,0,1,9],
[9,0,0,9,0,0,0,9,9],
[9]*9
]

d=[[0,-1],[-1,0],[0,1],[1,0]]

x,y=1,1

while maze[x][y]!=1:
    move=random.choice(d)
    if maze[x+move[0]][y+move[1]]!=9:
        x+=move[0]
        y+=move[1]
        print([x,y])
